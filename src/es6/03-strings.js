let hello = 'Hello';
let world = 'World';

let epicPhrase = hello + ' ' + world + '!';

// Template literals
let epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

// Multi-line strings
let lorem = 'esto es un string \n'  + 'que se puede usar para \n' + 'multiplas lineas';

let lorem2 = `esto es un string
que se puede usar para
multiplas lineas`;
console.log(lorem);
console.log(lorem2);
